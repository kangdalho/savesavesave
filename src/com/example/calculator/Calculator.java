package com.example.calculator;

public class Calculator {

    // 메서드 선언
    int sum(int a1, int b1) {
            int result = a1 + b1;
            return result;
    }
    int sub(int a1, int b1) {
            int result = a1 - b1;
            return result;
    }
    int mul(int a1, int b1) {
        int result = a1 * b1;
        return result;
    }
    int div(int a1, int b1) {
        int result = a1 / b1;
        return result;
    }



}
